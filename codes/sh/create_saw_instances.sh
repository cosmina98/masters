# THEY ARE USING A UNR_SAW? There are three internal saws
# on PYAsUKP, saw, saw2 and unr_saw. The only you can define
# a pMax is unr_saw...
ns=(10000 50000 50000 100000)
wmins=(10000 5000 10000 10000)
nbis=(200 500 200 200)

for l in 0 1 2 3; do
	n=${ns[l]}
	wmin=${wmins[l]}
	wmax="1$n"
	pmax="2$n"
	eval "cs2=(${cs[$l]})"
	for ((i=0; i < ${nbis[$l]}; ++i)); do
		# Random values between 1 and 5, given to pyasukp by the step
		# flag, we get the modulo five of the random numbers
		alpha=`expr \( ${seeds[$i]} % 5 \) + 1`
		c=${cs2[$i]}
		pyasukp -nosolve -form UnrSaw -save "saw_n${n}wmin${wmin}-$i-s${seeds[$i]}c$c.ukp" -step $alpha -seed ${seeds[$i]} -cap $c -wmin $wmin -wmax $wmax -pmax $pmax -n $n
	done
done

